function sum(a, b) {
    return a + b
}

function sub(a, b) {
    return a - b
}

function multi(a, b) {
    return a * b
}

function division(a, b) {
    return a / b
}

function showResult(a, b) {
    console.log(sum(a, b))
    console.log(sub(a, b))
    console.log(multi(a, b))
    console.log(division(a, b))
}

showResult(10, 2)